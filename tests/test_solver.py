def test_solve_easy_board():
    from sudoku.solver import solve
    from sudoku.utils import print_board

    easy_board = [
        [5, 3, 0, 0, 7, 0, 0, 0, 0],
        [6, 0, 0, 1, 9, 5, 0, 0, 0],
        [0, 9, 8, 0, 0, 0, 0, 6, 0],
        [8, 0, 0, 0, 6, 0, 0, 0, 3],
        [4, 0, 0, 8, 0, 3, 0, 0, 1],
        [7, 0, 0, 0, 2, 0, 0, 0, 6],
        [0, 6, 0, 0, 0, 2, 8, 4, 7],
        [2, 8, 7, 4, 1, 9, 6, 3, 5],
        [3, 4, 5, 2, 8, 6, 1, 9, 0]
    ]

    print("Original Board:")
    print_board(easy_board)

    assert solve(easy_board) is True

    print("Solved Board:")
    print_board(easy_board)